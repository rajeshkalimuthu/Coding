package Programs;

public class FactorialNumber {
	public static void main(String[] args) {
		int f=1; //start with 0ne for factorial number
		for(int i=1; i<=8;i++) {
			f=f*i;
			
		}
		System.out.println(f);
		
		
		
		int g=1;
		for(int j=1;j<10;j++) {
			g=g*j;
			
		}
		System.out.println(g);
		
	}
}
